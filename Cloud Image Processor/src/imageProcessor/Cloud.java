/**
 * Enumerates various cloud types.
 * @author Gage Davidson
 */
enum Cloud {
    
    /** Cirrus. */
    CIRRUS,
    
    /** Cumulus. */
    CUMULUS,
    
    /** Other or unknown. */
    OTHER,
    
    /** Stratus. */
    STRATUS,
    
    /** Waterspout. */
    WATERSPOUT
}
